package KotlinPractice

fun main() {

    //represents a number in kotlin
    var byte: Byte = 0 // 8bit
    var short: Short = 0 // 16bit
    var int: Int = 0 //32bit
    var long: Long = 0L //64bit

    var float: Float = 0F //32bit
    var double: Double = 0.0 //64bit

    println(byte)
    println(short)
    println(int)
    println(long)
    println(float)
    println(double)

    // strings
    var string: String = "string"
    var char: Char = 'C'
    println(string)
    println(char)

    //booleans
    var boolean: Boolean = false
    println(boolean)

}